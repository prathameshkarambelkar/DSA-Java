package Basics;

public class Primitives {
    public static void main(String[] args) {
        // Primitive is a datatype which we cant break further
        // Char starts with single quotes, string starts with double quotes
        int rollNo = 64;
        char letter = '4';
        float percent = 98.67f;
        double largeDecimalNumber = 34534534.34;
        long largeInteger = 323423423434L;
        boolean check = false;
        // String is not primitive
        // String can be broken into characters
    }
}
